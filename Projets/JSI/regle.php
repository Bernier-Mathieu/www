Les communes sont responsables de l'alimentation en eau pour la défense extérieure contre l'incendie sur le territoire communal (circulaire du 10 décembre 1951).<br><br>

Article 77<br><br>

Le même code est ainsi modifié :<br>
1° La section 4 du chapitre III du titre Ier du livre II de la deuxième partie est complétée par un article L. 2213-32 ainsi rédigé :<br>
« Art. L. 2213-32.-Le maire assure la défense extérieure contre l'incendie. » ;<br>
2° Le titre II du livre II de la deuxième partie est complété par un chapitre V ainsi rédigé :<br>
« Chapitre V<br>
« Défense extérieure contre l'incendie<br>
« Art. L. 2225-1.-La défense extérieure contre l'incendie a pour objet d'assurer, en fonction des besoins résultant des risques à prendre en compte, l'alimentation en eau des moyens des services d'incendie et de secours par l'intermédiaire de points d'eau identifiés à cette fin. Elle est placée sous l'autorité du maire conformément à l'article L. 2213-32.<br>
« Art. L. 2225-2.-Les communes sont chargées du service public de défense extérieure contre l'incendie et sont compétentes à ce titre pour la création, l'aménagement et la gestion des points d'eau nécessaires à l'alimentation en eau des moyens des services d'incendie et de secours. Elles peuvent également intervenir en amont de ces points d'eau pour garantir leur approvisionnement.<br>
« Art. L. 2225-3.-Lorsque l'approvisionnement des points d'eau visés aux articles L. 2225-1 et L. 2225-2 fait appel à un réseau de transport ou de distribution d'eau, les investissements afférents demandés à la personne publique ou privée responsable de ce réseau sont pris en charge par le service public de défense extérieure contre l'incendie.<br>
« Art. L. 2225-4.-Un décret en Conseil d'Etat détermine les conditions d'application du présent chapitre. » ;<br>
3° L'article L. 5211-9-2 est ainsi modifié :<br>
a) Le I est complété par un alinéa ainsi rédigé :<br>
« Sans préjudice de l'article L. 2212-2 et par dérogation aux dispositions de l'article L. 2213-32, lorsqu'un établissement public de coopération intercommunale à fiscalité propre est compétent en matière de défense extérieure contre l'incendie, les maires des communes membres de celui-ci peuvent transférer au président de cet établissement des attributions lui permettant de réglementer cette activité. » ;<br>
b) A la première phrase du premier alinéa du IV, les mots : « quatrième et dernier » sont remplacés par les mots : « trois derniers ».<br><br>
<font color="red">
Le besoin en eau demandé est de 120 M3, cette donnée correspond à la manœuvre de deux grosses lances pendant deux heures. Les hydrants, poteaux incendie ou bouches incendie doivent fournir 60 M3/Heure pendant deux heures.</font><br><br>
Les points d'eau sont les poteaux d'incendie, les bouches d'incendie et les réserves incendie. Si nécessaire, les rivières peuvent être utilisées. Exceptionnellement les mares peuvent faire usage de point d'eau, mais sont considérées comme non pérennes en fonction des périodes climatiques particulièrement chaudes.<br><br>

Une des questions les plus posées concerne la périodicité du contrôle de ces hydrants. Les hydrants doivent être vérifiés et pesés régulièrement suivant la périodicité arrêtée par le règlement opérationnel départemental.<br>
